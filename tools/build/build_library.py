# Grade glyph = the volume number 二…六; colour = its cover badge (COVER[2..6]).
GLYPH = {"二年级":"二","三年级":"三","四年级":"四","五年级":"五","六年级":"六"}
PY = {g:[[g[0],{"二":"èr","三":"sān","四":"sì","五":"wǔ","六":"liù"}[g[0]]],["年","nián"],["级","jí"]] for g in GRADES}
NUM = {"二年级":2,"三年级":3,"四年级":4,"五年级":5,"六年级":6}
VOLCN = {2:"第二册",3:"第三册",4:"第四册",5:"第五册",6:"第六册"}

# Years 2–6 fold into the same 中文 set as Books 2–6 (第二册–第六册).
for grade,lines in GRADES.items():
    n=NUM[grade]; c=COVER[n]
    lessons=[{"n":i+1,"title":f"第{i+1}课 · Lesson {i+1}","chars":list(line)} for i,line in enumerate(lines)]
    ZW["books"].append({"id":f"G{n}","title":f"Zhōngwén Book {n}","cn":"中文","cnpy":PY[grade],
                  "vol":VOLCN[n],"glyph":GLYPH[grade],"pigment":c["pigment"],
                  "accent":c["accent"],"accentSoft":c["accentSoft"],"tint":c["tint"],"lessons":lessons})

# complete ZW1: append the 45 previously-missing chars to their lessons (deduped)
for lesson in ZW["books"][0]["lessons"]:
    extra=ZW1_ADD.get(lesson["n"])
    if extra:
        for ch in extra:
            if ch not in lesson["chars"]:
                lesson["chars"].append(ch)

