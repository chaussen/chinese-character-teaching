#!/usr/bin/env python3
"""Fetch stroke data (hanzi-writer-data CDN) + pinyin/meaning/radical
(makemeahanzi dictionary) for the curriculum chars missing from CHAR_INDEX,
and write learn/library-chars.js (window.LIBRARY_CHARS = {chars:[...]})."""
import json, re, sys, ssl, urllib.request, urllib.parse, os
from concurrent.futures import ThreadPoolExecutor, as_completed
sys.path.insert(0, os.path.dirname(__file__))
from curriculum import GRADES

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
CTX = ssl.create_default_context(cafile='/etc/ssl/certs/ca-certificates.crt')
CDN = "https://cdn.jsdelivr.net/npm/hanzi-writer-data@2.0/"

def load_dict():
    d = {}
    for line in open(os.path.join(ROOT,'tools/build/dictionary.txt'), encoding='utf-8'):
        line=line.strip()
        if not line: continue
        o=json.loads(line)
        d[o['character']]=o
    return d

def short_en(defn):
    if not defn: return ""
    # first sense before ; — drop bracketed/parenthetical noise, trim length
    s = defn.split(';')[0].strip()
    s = re.sub(r'\s+', ' ', s)
    if len(s) > 42:
        s = s[:42].rsplit(' ',1)[0] + '…'
    return s

def fetch_strokes(ch):
    url = CDN + urllib.parse.quote(ch) + ".json"
    for _ in range(4):
        try:
            return json.loads(urllib.request.urlopen(url, context=CTX, timeout=25).read())
        except Exception as e:
            last=e
    raise last

def main():
    DICT = load_dict()
    have = set()
    for p in ['learn/app-data.js','learn/general-data.js']:
        have |= set(re.findall(r'"ch"\s*:\s*"(.)"', open(os.path.join(ROOT,p),encoding='utf-8').read()))
    allc=[]
    for lines in GRADES.values():
        for l in lines: allc+=list(l)
    missing=[c for c in dict.fromkeys(allc) if c not in have]
    print(f"fetching {len(missing)} chars...")

    out={}
    fails=[]
    def work(ch):
        g = fetch_strokes(ch)
        meta = DICT.get(ch, {})
        py = (meta.get('pinyin') or [""])[0]
        rec = {"ch":ch, "py":py, "en":short_en(meta.get('definition','')),
               "strokes":len(g['strokes']), "radical":meta.get('radical',''),
               "s":g['strokes'], "m":g['medians']}
        return ch, rec
    with ThreadPoolExecutor(max_workers=12) as ex:
        futs={ex.submit(work,c):c for c in missing}
        for i,f in enumerate(as_completed(futs),1):
            c=futs[f]
            try:
                ch,rec=f.result(); out[ch]=rec
            except Exception as e:
                fails.append((c,str(e)))
            if i%50==0: print(f"  {i}/{len(missing)}")
    print(f"done: {len(out)} ok, {len(fails)} failed")
    for c,e in fails: print("  FAIL",c,e)
    # preserve curriculum order
    ordered=[out[c] for c in missing if c in out]
    save(ordered, fails)

HEADER = """\
/* library-chars.js — 学写字 · Character Studio · Library stroke-data pool.
   ▸ AUTO-GENERATED by tools/build/fetch_chars.py.
   Stroke paths/medians: hanzi-writer-data (makemeahanzi, 1024 Y-up).
   pinyin / meaning / radical: makemeahanzi dictionary.
   These characters carry stroke data so the grade-based 中文 books in
   library-data.js can list them; they are NOT a Home theme of their own
   (only feed CHAR_INDEX — see buildIndex in app.js). */
"""
def save(ordered, fails):
    body = json.dumps({"chars":ordered}, ensure_ascii=False)
    open(os.path.join(ROOT,'learn/library-chars.js'),'w',encoding='utf-8').write(
        HEADER + "window.LIBRARY_CHARS = " + body + ";\n")
    if fails:
        open(os.path.join(ROOT,'tools/build/fetch_fails.txt'),'w',encoding='utf-8').write(
            ''.join(c for c,_ in fails))

if __name__=="__main__":
    main()
